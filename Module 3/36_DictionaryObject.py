# 36. How Do You Traverse Through A Dictionary Object In Python?

student = {
    'name':'Darshan',
    'roll_no':97,
    'address':'nikol',
    'std':12,
}
# Travers Keys
print("\nKeys:")
for i in student.keys():
    print(i)
# Travers Keys
print("\nKeys:")
for i in student:
    print(i)

# Travers Values
print("\nValues:")
for i in student.values():
    print(i)